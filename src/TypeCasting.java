public class TypeCasting {
    public static void main(String[] args) {
        // Type casting?
        // convert one type of data into another type

        // Implicit casting
        double data1 = 52;
        System.out.println(data1);
        System.out.println("Abc" + 258);

        // Explicit casting
        int data2 = (int) 59.3F;
        System.out.println(data2);

    }
}
